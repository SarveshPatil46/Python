# CHECK WHETHER A NUMBER IS POSITIVE, NEGATIVE OR ZERO

input_num = float(input("Enter a number : "))
if input_num > 0:
    print(f"{input_num} is a positive number")
elif input_num == 0:
    print(f"{input_num} is zero")
else:
    print(f"{input_num} is a negative number")
